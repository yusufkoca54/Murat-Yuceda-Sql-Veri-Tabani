-- Her bir ürünÜN KAÇ ADET DEÐÝL KAÇ FARKLI MÜÞTERÝYE SATILAN SORGU

select URUNAD,count (*) AS 'URUN AD' from TBLHAREKET
inner join TBLURUNLER
ON TBLHAREKET.URUN=TBLURUNLER.URUNID
GROUP BY URUNAD ORDER BY COUNT (*) 



-- ÜRÜNLER TABLOSU ÝÇERÝSÝNDE SADECECE BÝLGÝSAYAR KATEGORÝSÝNDE SATILMIÞ OLAN ÜRÜNLERÝ GETÝREN SORGU


SELECT * FROM TBLHAREKET WHERE URUN IN(SELECT URUNID FROM TBLURUNLER WHERE KATEGORI =1)












